#include <unistd.h>

void ft_print_comb2(void)
{
	char a,b,l,m;
	a = '0';
	b = '0';
	l = '0';
	m = '1';
	
	while (a <= '9')
	{
		while (b <= '9')
		{
			while (l <= '9')
			{
				while (m <= '9')
				{
					if ((a == l) && (b == m))
					{
					}
					else
					{
					write(1, &a, 1);
					write(1, &b, 1);
					write(1, &" ", 1);
					write(1, &l, 1);
					write(1, &m, 1);
					write(1, ", ", 2);
					}
					m++;
				}
				m = '0';
				l++;
			}
			b++;
			m = b + 1;
			l = '0';
		}
		a++;
		b = '0';
	}
}

int main()
{
	ft_print_comb2();
	return(0);
}
